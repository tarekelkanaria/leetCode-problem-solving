// Problem 88 Merge Sorted Array
var merge = function (nums1, m, nums2, n) {
  // make the array contain only the numbers and remove empty elements
  nums1.length = m;
  nums2.length = n;
  // push the array two inyo array 1 as elements
  nums1.push(...nums2);
  // sort the array one in non-decreasing order
  nums1.sort((a, b) => a - b);
  return nums1;
};
console.log(merge([1, 2, 3, 0, 0, 0], 3, [2, 5, 6], 3)); // [1,2,2,3,5,6]
console.log(merge([1], 1, [], 0)); // [1]
console.log(merge([0], 0, [1], 1)); // [1]
console.log(merge([-1, 0, 0, 3, 3, 3, 0, 0, 0], 6, [1, 2, 2], 3)); // [1]
console.log(merge([2, 0], 1, [1], 1)); // [1, 2]
